x=int(input("enter any value:"))
y=int(input("enter any value:"))
#arthmatic operators
print((x+y),(x-y),(x%y),(x*y),(x**y),(x/y))

#comparision operators
print((x<y),(x>y),(x<=y),(x>=y),(x==y),(x!=y))

#logical operators
print(x<y and x!=y)
print(x<y or x!=y)
print(not x>y and x==y)

#assignment operators
x1=x+y;x2=x-y;x3=x*y;x4=x/y;x5=x%y
print(x1,x2,x3,x4,x5)
